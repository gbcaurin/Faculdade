#Elabore um programa que leia dois numeros inteiros e mostre a parte inteira da divisao entre eles e o resto da divisao entre eles.
num1 = int(input("Digite um numero inteiro:"))
num2 = int(input("Digite outro numero inteiro:"))
print("A parte inteira da divisao entre os numeros é:", num1 // num2)
print("O resto da divisao entre os dois numeros é:", num1 % num2)